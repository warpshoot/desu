let currentLang = localStorage.getItem('ink-lang') || 'ja';

export function getLang() {
    return currentLang;
}

export function setLang(lang) {
    if (dict[lang]) {
        currentLang = lang;
        localStorage.setItem('ink-lang', lang);
        applyTextToDOM();
    }
}

export function t(key) {
    return dict[currentLang]?.[key] || key;
}

export function applyTextToDOM() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (dict[currentLang]?.[key]) el.textContent = dict[currentLang][key];
    });

    document.querySelectorAll('[data-i18n-title]').forEach(el => {
        const key = el.getAttribute('data-i18n-title');
        if (dict[currentLang]?.[key]) el.setAttribute('title', dict[currentLang][key]);
    });
}
